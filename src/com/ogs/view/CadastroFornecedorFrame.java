    private void tblFornecedoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblFornecedoresMouseClicked
        JTable source = (JTable)evt.getSource();
        int row = source.rowAtPoint( evt.getPoint() );

        String s = source.getModel().getValueAt(row, 0)+"";

        Fornecedor fornecedor = new Fornecedor();
        fornecedor.setId(Integer.parseInt(s));

        try {
            this.connection.executarSQL(
                "SELECT nome, cnpj, endereco, telefone, email"
                + " FROM "
                + "Fornecedor"
                + " WHERE "
                + "id="
                + fornecedor.getId()
                + ";"
            );

            while(this.connection.getResultSet().next()){
                fornecedor.setNome(this.connection.getResultSet().getString(1));
                fornecedor.setCnpj(this.connection.getResultSet().getString(2));
                fornecedor.setEndereco(this.connection.getResultSet().getString(3));
                fornecedor.setTelefone(this.connection.getResultSet().getString(4));
                fornecedor.setEmail(this.connection.getResultSet().getString(5));
            }
        }
        catch (Exception e) {
            System.out.println("Erro ao consultar cliente " +  e.getMessage());
        }
        finally {
            txtNome.setText(fornecedor.getNome());
            txtCNPJ.setText(fornecedor.getCnpj());
            txtEndereco.setText(fornecedor.getEndereco());
            txtTelefone.setText(fornecedor.getTelefone());
            txtEmail.setText(fornecedor.getEmail());
            currentId = fornecedor.getId();
            btnAlterar.setEnabled(true);
            btnRemover.setEnabled(true);
        }
    }//GEN-LAST:event_tblFornecedoresMouseClicked

