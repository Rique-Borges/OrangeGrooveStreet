    private void reloadTable()
    {   
        this.connection.conectaBanco();
        
        Fornecedor fornecedor = new Fornecedor();
                
        DefaultTableModel dtm = (DefaultTableModel) tblFornecedores.getModel();
     
        dtm.setRowCount(0);
        
        try {
            this.connection.executarSQL(
                "SELECT * FROM Fornecedor;"
            );
            
            while(this.connection.getResultSet().next()){
                fornecedor.setId(this.connection.getResultSet().getInt(1));
                fornecedor.setNome(this.connection.getResultSet().getString(2));
                fornecedor.setCnpj(this.connection.getResultSet().getString(3));
                fornecedor.setEndereco(this.connection.getResultSet().getString(4));
                fornecedor.setTelefone(this.connection.getResultSet().getString(5));
                fornecedor.setEmail(this.connection.getResultSet().getString(6));
                
                Object[] row = {
                    fornecedor.getId(),
                    fornecedor.getNome(),
                    fornecedor.getCnpj(),
                    fornecedor.getEndereco(),
                    fornecedor.getTelefone(),
                    fornecedor.getEmail(),
                };
                
                dtm.addRow(row);
           }
        } catch (Exception e) {            
            System.out.println("Erro ao consultar cliente " +  e.getMessage());
        }
    }
